void main() {
  var a;
  print(a);
  a = 10;
  print(a);
  a = "hello";
  print(a);
  var b = 'world';
  print(b);
  //final 只能赋值一次的变量，申明时必须赋值
  final c = "hackerzzm";
  //c = 10;
  print(c);
  //
  const d = 27;
  //d = 38;
  print(d);
  /**
    https://www.jianshu.com/p/94d3fde57dfd
    Dart中的static、final、const
   */
}
